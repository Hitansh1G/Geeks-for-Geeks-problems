class Solution{
  public:
    //Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
        // your code here
        vector<int> v;
        Node* temp=head;
        while(temp!=NULL){
            v.push_back(temp->data);
            temp=temp->next;
        }
        sort(v.begin(),v.end());
        Node* root=new Node(0);
        Node* temp1=root;
        for(int i=0;i<v.size();i++){
            Node* ans=new Node(v[i]);
            temp1->next=ans;
            temp1=temp1->next;
        }
        return root->next;
    }
};


